package models

import "testing"

func TestIsValidStatus(t *testing.T) {
	allowed := []string{"ACTIVE", "IN-PROGRESS", "COMPLETED"}

	tests := []struct {
		status   string
		expected bool
	}{
		{"ACTIVE", true},
		{"active", true},
		{"IN-PROGRESS", true},
		{"in_progress", true},
		{"COMPLETED", true},
		{"BACKLOG", false},
		{"unknown", false},
	}

	for _, tt := range tests {
		if got := IsValidStatus(tt.status, allowed); got != tt.expected {
			t.Errorf("IsValidStatus(%q) = %v; want %v", tt.status, got, tt.expected)
		}
	}
}
